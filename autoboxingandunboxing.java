public class autoboxingandunboxing {

int i = 10;
Integer wrapper = i; // autoboxing
int j = wrapper; // unboxing

    
}

/*
 * In summary, autoboxing converts a primitive type to its corresponding wrapper class 
 * type, and unboxing converts an object of a wrapper class type to its corresponding primitive type. 
 * Both conversions occur automatically at compile time and
 *  do not require any explicit action from the programmer.
 */